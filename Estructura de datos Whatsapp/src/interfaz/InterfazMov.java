    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed
        /*int i = JOptionPane.showConfirmDialog(this,
                "¿Realmente desea mover a '" + PantallaPrincipal.getSelectedPerso().toUpperCase() + "'\nal grupo '" + grupoSeleccionado + "'?");*/
        int ii = JOptionPane.showConfirmDialog(null, "¿Realmente desea mover a '" + PantallaPrincipal.getSelectedPerso().toUpperCase() + "'\nal grupo '" + grupoSeleccionado + "'?",
                "Confirme que desea mover", 0, 0);
        if (ii == 0)
        {
            PantallaPrincipal.pnlPrincipal.setVisible(true);
            NodoMulti arr[] = new NodoMulti[2];
            String etqs[] = new String[2];
            etqs[0] = PantallaPrincipal.getSelectedGrupo();
            etqs[1] = PantallaPrincipal.getSelectedPerso();
            arr = Multilistas.eliminar(PantallaPrincipal.getR(), 0, etqs, arr);
            PantallaPrincipal.setR(arr[0]);
            etqs[0] = grupoSeleccionado;
            etqs[1] = PantallaPrincipal.getSelectedPerso();

            NodoMulti arrb = Multilistas.buscar(PantallaPrincipal.getR(), etqs[0]);
            PantallaPrincipal.setR(Multilistas.insertar(PantallaPrincipal.getR(), arr[1], arrb, 0, etqs));
            this.dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

