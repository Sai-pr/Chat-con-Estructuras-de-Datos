    public void despBtns()
    {
        NodoMulti aux = PantallaPrincipal.getR();
        int index = 0;
        while (aux != null)
        {
            if (!aux.getEtq().equals(PantallaPrincipal.getSelectedGrupo()))
            {
                arrBtn[index] = new JButton();
                arrBtn[index].setBounds(x, y, an, al);
                arrBtn[index].setText(aux.getEtq());
                arrBtn[index].setBackground(Color.white);
                arrBtn[index].addActionListener(this);
                jPnlBtns.add(arrBtn[index]);
                y += 45;
            }
            aux = aux.getSig();
            index++;
        }
        if (arrBtn.length > 5)
        {
            jPnlBtns.setPreferredSize(new Dimension(245, 250 + 45 * (arrBtn.length - 6)));
        } else
        {
            jPnlBtns.setPreferredSize(new Dimension(245, 250));
        }
        y = 30;
    }

